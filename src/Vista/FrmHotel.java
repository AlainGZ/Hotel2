        String fechaTextoFin = txtfechafin.getText();
        // Definir el formato de la fecha
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyyy");

        try {
            // Convertir el texto a LocalDate
            LocalDate fechaInicio = LocalDate.parse(fechaTexto, formatter);
            LocalDate fechaFin = LocalDate.parse(fechaTextoFin, formatter);

            // Verificar que fechaInicio es anterior o igual a fechaFin
            if (fechaInicio.isAfter(fechaFin)) {
                JOptionPane.showMessageDialog(null, "Error: La fecha de inicio debe ser anterior o igual a la fecha de fin.");
                return; // Salir del método si las fechas no son válidas
            }

            if (hotel.isHabitacionDisponible(habitacion.getNumeroHabitacion(), fechaInicio, fechaFin)) {

                Reserva reserva1 = new Reserva(huesped1, habitacion, fechaInicio, fechaFin);

                hotel.addReserva(reserva1);
                JOptionPane.showMessageDialog(null, "Reservacion agregada con exito, el costo del servicio es de: " + reserva1.getCostoTotal() + " a nombre de: " + reserva1.getHuesped() + " habitacion " + habitacion);

            } else {
                JOptionPane.showMessageDialog(null, "La habitacion no esta disponible");
            }
           

        } catch (DateTimeParseException e) {
            // Manejar el error si el formato de la fecha no es válido
            JOptionPane.showMessageDialog(null, "Error: Formato de fecha incorrecto.");
        }

