    if (habitacionesTipoSeleccionado != null && !habitacionesTipoSeleccionado.isEmpty()) {
        // Asignar la primera habitación disponible
        habitacion = habitacionesTipoSeleccionado.remove(0);
        hotel.addHabitacion(habitacion); // Añadir la habitación al hotel
    } else {
        JOptionPane.showMessageDialog(null, "No hay habitaciones disponibles del tipo seleccionado.");
        return; // Salir si no hay habitaciones disponibles
    }

